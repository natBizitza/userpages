import { useEffect } from 'react';
import { useTable, usePagination } from 'react-table';
import styles from '../styles/table.module.scss';

type Props = {
  columns: any;
  data: any;
  pageCount: number;
  fetchData: ({ pageIndex }: any) => Promise<void>;
};

